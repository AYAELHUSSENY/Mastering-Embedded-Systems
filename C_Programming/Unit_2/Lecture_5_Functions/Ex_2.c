#include"stdio.h"
int factorial(int num);
void main()
{
    int number;
    printf("Enter a positive integer:");
    scanf("%d",&number);
    printf("Factorial of %d = %d",number, factorial(number));
}
int factorial(int num)
{

    if(num > 1)
    {
         return num * factorial(num-1);
    }
    else
    {
        return 1;
    }

}
